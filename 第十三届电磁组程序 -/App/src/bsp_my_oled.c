/**
 *  @ brief   oled_display_6x8char
 *
 *  @ param   uint8 uc_posx  显示的横坐标0~122
 * 			  uint8 uc_posy  页范围0～7
 *    		  uint8 uc_data  显示的字符
 *
 *	@ note    display a charactchar
 *
 *	@ return  none
 */
void oled_display_6x8char(uint8 uc_posx, uint8 uc_posy, char uc_data)
{
    uint8 i, uc_dataTmp;

    uc_dataTmp = uc_data - 32;
    if(uc_posx > 122)
    {
        uc_posx = 0;
        uc_posy ++;
    }

    LCD_Set_Pos(uc_posx, uc_posy);

    for(i = 0; i < 6; i++)
    {
        LCD_WrDat(F6x8[uc_dataTmp][i]);
    }
}


/**
 *  @ brief   oled_display_6x8str
 *
 *  @ param   uint8 uc_posx       显示的横坐标0~122
 *			  uint8 uc_posy       页范围0～7
 *    		  uint8 uc_dataStr[]  显示的字符串
 *
 *	@ note    display a string
 *
 *	@ return  none
 */
void oled_display_6x8str(uint8 uc_posx, uint8 uc_posy, char uc_dataStr[])
{
    uint8 i, j, uc_dataTmp;

    for (j = 0; uc_dataStr[j] != '\0'; j++)
    {
        uc_dataTmp = uc_dataStr[j] - 32;

        if(uc_posx > 122)
        {
            uc_posx = 0;
            uc_posy++;
        }

        LCD_Set_Pos(uc_posx,uc_posy);

        for(i = 0; i < 6; i++)
        {
            LCD_WrDat(F6x8[uc_dataTmp][i]);
        }

        uc_posx += 6;
    }

    return;
}


/**
 *  @ brief   oled_print_char
 *
 *  @ param   uint8 uc_posx  显示的横坐标0~122
 *    		  uint8 uc_posy  页范围0～7
 *    		  int8  c_data   需要转化显示的数值 -128~127
 *
 *	@ note    将一个Char型数转换成3位数进行显示
 *
 *	@ return  none
 */
void oled_print_char(uint8 uc_posx, uint8 uc_posy, char c_data)
{
    uint16 i, j, k, us_data;

    if(c_data < 0)
    {
    	oled_display_6x8char(uc_posx, uc_posy, '-');
        us_data = (uint16)(-c_data);
    }
    else
    {
    	oled_display_6x8char(uc_posx, uc_posy, '+');
        us_data = (uint16)c_data;
    }

    i =  us_data / 100;
    j = (us_data % 100) / 10;
    k =  us_data % 10;

    oled_display_6x8char(uc_posx+6, uc_posy, i+48);
    oled_display_6x8char(uc_posx+12, uc_posy, j+48);
    oled_display_6x8char(uc_posx+18, uc_posy, k+48);

    return;
}

